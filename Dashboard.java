    //Create a function to add border to all the button in the jPanel Menu
    public void addBorder(){
        //get all the components in the jPanel menu,
        Component[] comps = jPanel_Menu.getComponents();
        
        for(Component comp : comps){
            //check if the components is a button
            if(comp instanceof JButton){
                JButton button = (JButton) comp;
                
                //Add border
                button.setBorder(buttonBorder0);
            }
        }
    }
    
    //Create a function to create hover effect on menu buttons
    public void buttonHoverEffect(){
        //get all the components in the jPanel menu,
        Component[] comps = jPanel_Menu.getComponents();
        
        for(Component comp : comps){
            //check if the components is a button
            if(comp instanceof JButton){
                JButton button = (JButton) comp;
                
                //Add the action we want to be button
                button.addMouseListener(new MouseAdapter()  {
                   
                    @Override
                    public void mouseEntered(MouseEvent evt){
                        button.setBorder(buttonBorder1);
                    }
                    
                    @Override
                    public void mouseExited(MouseEvent evt){
                        button.setBorder(buttonBorder0);
                    }
                });
            }
        }
    }
    
