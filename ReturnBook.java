    private void jButton_LostActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_LostActionPerformed
        //Book is lost 
        int _book_id = (int) jSpinner_getBookID.getValue();
        int _member_id = (int) jSpinner_getMemberID.getValue();
        String _note = jTextArea_Note.getText();
        
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");       

        try{
            String return_date = dateFormat.format(jDateChooser_ReturnDate.getDate());
            String issue_date = dateFormat.format(jDateChooser_IssueDate.getDate());
           
            issue.updateIssue(_book_id, _member_id, "LOST", issue_date, return_date, _note);
            
            //Reducing QUANTITY of book by 1
            float quantity = new genre().getBookById(_book_id).getQuantity();
            genre.setQuantityMinusOne(_book_id, quantity-1);
                
            //Reset Fields
            jSpinner_getBookID.setValue(0);
            jSpinner_getMemberID.setValue(0);
            jLabel_BookName.setText("Book Name");
            jLabel_MemberFullName.setText("Member Full Name");
            jDateChooser_IssueDate.setDate(null);
            jDateChooser_ReturnDate.setDate(null);
            jTextArea_Note.setText("");
                
        } catch(HeadlessException | NullPointerException ex){
            JOptionPane.showMessageDialog(null, "Select an item from the table", "Select Item", 2);
        } catch (SQLException ex) {
            Logger.getLogger(returnBook.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton_LostActionPerformed

