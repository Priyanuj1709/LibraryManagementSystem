    //Create a function to populate the jTable with issued books.
    public void populateJTableWithIssuedBook(String STATUS){
        ArrayList<issue_Book> issBookList = issue.issueBookList(STATUS);
        
        //jTable columns
        String[] colName = {"BookID", "MemberID", "Status", "Isd Date", "Rtn Date", "Note"};
        
        //row
        Object[][] row = new Object[issBookList.size()][colName.length];
        
        for(int i = 0; i < issBookList.size(); i++){
            row[i][0] = issBookList.get(i).getBookID();
            row[i][1] = issBookList.get(i).getMemberID();
            row[i][2] = issBookList.get(i).getStatus();
            row[i][3] = issBookList.get(i).getIssueBook();
            row[i][4] = issBookList.get(i).getReturnBook();
            row[i][5] = issBookList.get(i).getNote();
        }
        
        DefaultTableModel model = new DefaultTableModel(row, colName);
        jTable_ReturnBook.setModel(model);
    }
    
    
