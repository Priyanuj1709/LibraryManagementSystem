    //Create a function to populate the jTable with books.
    public void populateJTableWithGenre(){
        ArrayList<genre> genreList = genre.genreList();
        
        //jTable columns
        String[] colName = {"ID", "NAME", "Author's Name", "Quantity", "Publisher", "Price"};
        
        //row
        Object[][] row = new Object[genreList.size()][colName.length];
        
        for(int i = 0; i < genreList.size(); i++){
            row[i][0] = genreList.get(i).getId();
            row[i][1] = genreList.get(i).getName();
            row[i][2] = genreList.get(i).getAuthorName();
            row[i][3] = genreList.get(i).getQuantity();
            row[i][4] = genreList.get(i).getPublisher();
            row[i][5] = genreList.get(i).getPrice();
        }
        
        DefaultTableModel model = new DefaultTableModel(row, colName);
        jTable_Genre.setModel(model);
    }
    
    
