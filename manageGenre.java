    private void jTable_GenreMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_GenreMouseClicked
        //Display the selected genre.
        //Get the selected row index
        int index = jTable_Genre.getSelectedRow();

        //Get values
        String id = jTable_Genre.getValueAt(index, 0).toString();
        String name = jTable_Genre.getValueAt(index, 1).toString();
        String authorName = jTable_Genre.getValueAt(index, 2).toString();
        String Quantity = jTable_Genre.getValueAt(index, 3).toString();
        String Publisher = jTable_Genre.getValueAt(index, 4).toString();
        String Price = jTable_Genre.getValueAt(index, 5).toString();

        //Show data in textFields
        jTextField_ID.setText(id);
        jTextField_Name.setText(name);
        jTextField_AuthorName.setText(authorName);
        jTextField_Quantity.setText(Quantity);
        jTextField_Publisher.setText(Publisher);
        jTextField_Price.setText(Price);
    }//GEN-LAST:event_jTable_GenreMouseClicked

    private void jButton_DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_DeleteActionPerformed

        //Delete the selected group.
        try{
            int id = Integer.valueOf(jTextField_ID.getText());
            genre.removeGenre(id);

            //Refresh the jTable book
            populateJTableWithGenre();

            //Clear the text from the textField
            jTextField_ID.setText("");
            jTextField_Name.setText("");
            jTextField_AuthorName.setText("");
            jTextField_Quantity.setText("");
            jTextField_Publisher.setText("");
            jTextField_Price.setText("");
        } catch(NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(null, "Invalid Genre ID - " + ex.getMessage(), "ERROR", 0);
        }

    }//GEN-LAST:event_jButton_DeleteActionPerformed

    private void jButton_AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_AddActionPerformed
        //Add a new genre
        String id = jTextField_ID.getText();
        String name = jTextField_Name.getText();
        String authorName = jTextField_AuthorName.getText();
        String Quantity = jTextField_Quantity.getText();
        String Publisher = jTextField_Publisher.getText();
        String Price = jTextField_Price.getText();

        //Check if  the textField is empty.
        if(name.isEmpty()){
            jLabel_EmptyName.setVisible(true);
        }

        //Check if  the textField is not empty.
        else{
            genre.addGenre(id, name, authorName, Quantity, Publisher, Price);

            //Refresh the jTable book
            populateJTableWithGenre();
        }
    }//GEN-LAST:event_jButton_AddActionPerformed

